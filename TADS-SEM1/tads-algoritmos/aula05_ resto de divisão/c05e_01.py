# 1. Dado um número inteiro, qual é o quociente da divisão desse número por dois e por três?

numero = int(input("Digite um número: "))

quocientePor2 = numero // 2
quocientePor3 = numero // 3

print("O quociente da divisão de ", numero, " por dois é ", quocientePor2, " e três por ", quocientePor3)
