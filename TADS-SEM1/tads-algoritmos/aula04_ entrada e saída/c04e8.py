# 8. A família Silva vai passar as férias na Europa e quer saber quantos euros vale o dinheiro em real que ela levará. Dados um valor na moeda real e a cotação do euro, qual o valor correspondente em euros?

valorReal = float(input("Digite o valor em R$: "))

conversaoEuro = valorReal * 0.19

print("O valor R$ ", valorReal, " equivale a € ", conversaoEuro)